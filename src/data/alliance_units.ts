/** Get unique origins from unit list */
export function getOrigins(units: Unit[]): string[] {
  return [...new Set(units.map(u => u.origin))].sort();
}

/** Get unique types from unit list */
export function getTypes(units: Unit[]): string[] {
  return [...new Set(units.map(u => u.type))].sort();
}
